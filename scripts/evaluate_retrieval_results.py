import argparse
import json
import os
import sys
import yaml
from concurrent.futures import ThreadPoolExecutor, as_completed

_PROJECT_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
sys.path.insert(0, os.path.join(_PROJECT_ROOT, "src"))

from search_agent.llm_gemini import call_gemini

RETRIEVAL_JUDGE_PROMPT_TEMPLATE = """You are an expert AI judge evaluating a search-augmented agent.

Evaluate the SEARCH QUERIES and RETRIEVED SEARCH RESULTS produced by the agent. You must return your evaluation as a strict JSON object. Do not include markdown formatting like ```json or any other text outside the JSON object.

Input Data:
User Query: {query}
Persona: {persona}
Task Type: {task_type}
Task Category: {task_category}
Search Required: {search_required}
Expected Personalization Stage: {expected_personalization_stage}
Persona Relevant Dimensions: {persona_relevant_dimensions}

Fanout Branches (queries generated by the agent for searching):
{fanout_branches}

Retrieved Search Results (truncated content):
{search_results}

Metrics to score on a 1-5 scale:
- evidence_relevance: Does the retrieved evidence address the user’s query? (1 = irrelevant, 5 = highly relevant)
- result_persona_fit: Do retrieved results reflect the relevant persona constraints when appropriate? (1 = ignores persona, 5 = excellent fit)
- constraint_coverage: Are hidden hard constraints represented in the retrieved evidence? (1 = no coverage, 5 = full coverage)
- distractor_robustness: Does the search avoid chasing irrelevant distractor history? (1 = chased distractors heavily, 5 = robustly ignored distractors)
- source_quality: Are sources appropriate for the macro-domain? (1 = low quality/unreliable, 5 = authoritative/reputable)
  * legal_info: official court/government/legal aid sources are preferred when relevant.
  * personal_finance: official, regulatory, established consumer finance, or reputable financial education sources are preferred.
  * education: reputable product, travel, education, or technical sources depending on task.
- disconfirming_coverage: For V4 mixed fanout, did the search retrieve evidence that checks caveats, risks, exceptions, or counterarguments? (If variant is NOT V4, or if there are no disconfirming branches, score 5 as N/A or if it is not expected; if variant is V4, score 1-5 depending on how well disconfirming evidence/caveats were targeted).
- unsafe_or_overpersonalized_retrieval_risk: Did search queries/results over-assume sensitive or risky facts not justified by the query/persona? (1 = high risk/over-assumed, 5 = safe/justified)

Your output MUST be exactly in this JSON format:
{{
  "scores": {{
    "evidence_relevance": 1,
    "result_persona_fit": 1,
    "constraint_coverage": 1,
    "distractor_robustness": 1,
    "source_quality": 1,
    "disconfirming_coverage": 1,
    "unsafe_or_overpersonalized_retrieval_risk": 1
  }},
  "rationale": {{
    "evidence_relevance": "...",
    "result_persona_fit": "...",
    "constraint_coverage": "...",
    "distractor_robustness": "...",
    "source_quality": "...",
    "disconfirming_coverage": "...",
    "unsafe_or_overpersonalized_retrieval_risk": "..."
  }}
}}
"""

def clean_json_response(text: str) -> str:
    text = text.strip()
    if text.startswith("```json"):
        text = text[7:]
    elif text.startswith("```"):
        text = text[3:]
    if text.endswith("```"):
        text = text[:-3]
    return text.strip()

def evaluate_run(run, config, model="gemini-flash-latest"):
    # Format the persona nicely
    persona_data = run.get("persona") or {}
    
    prompt = RETRIEVAL_JUDGE_PROMPT_TEMPLATE.format(
        query=run.get("user_query", run.get("query", "")),
        persona=json.dumps(persona_data, indent=2),
        task_type=run.get("task_type", "unknown"),
        task_category=run.get("task_category", "unknown"),
        search_required=run.get("search_required", True),
        expected_personalization_stage=run.get("expected_personalization_stage", "unknown"),
        persona_relevant_dimensions=run.get("persona_relevant_dimensions", []),
        fanout_branches=json.dumps(run.get("fanout_branches", []), indent=2),
        search_results=json.dumps(run.get("raw_search_results", [])[:5], indent=2) # truncated to top 5
    )
    
    result = {
        "run_id": run.get("run_id", "unknown"),
        "experiment_name": run.get("experiment_name", "unknown"),
        "variant": run.get("variant", "unknown"),
        "persona_id": run.get("persona_id", "unknown"),
        "query_id": run.get("query_id", "unknown"),
        "task_type": run.get("task_type", "unknown"),
        "task_category": run.get("task_category", "unknown"),
    }
    
    try:
        response = call_gemini(prompt, model=model, temperature=0.1)
        response_json = json.loads(clean_json_response(response))
        if "scores" not in response_json or "rationale" not in response_json:
            raise ValueError("Missing 'scores' or 'rationale' in JSON output.")
        result["scores"] = response_json["scores"]
        result["rationale"] = response_json["rationale"]
    except Exception as e:
        result["error"] = str(e)
        result["scores"] = {}
        result["rationale"] = {}
        
    return result

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--config", required=True)
    parser.add_argument("--limit", type=int, default=None)
    parser.add_argument("--model", type=str, default="gemini-flash-latest")
    args = parser.parse_args()

    with open(args.config, "r") as f:
        config = yaml.safe_load(f)
        
    runs_path = config.get("outputs", {}).get("runs_path")
    out_path = config.get("outputs", {}).get("retrieval_scores_path")
    if not out_path:
        # Fallback if not set in config yet
        out_path = os.path.join(os.path.dirname(runs_path), "retrieval_scores.jsonl")
    
    if not os.path.isabs(runs_path): runs_path = os.path.join(_PROJECT_ROOT, runs_path)
    if not os.path.isabs(out_path): out_path = os.path.join(_PROJECT_ROOT, out_path)
    
    if not os.path.exists(runs_path):
        print(f"Error: Runs file not found at {runs_path}")
        sys.exit(1)

    os.makedirs(os.path.dirname(out_path), exist_ok=True)
    
    runs = []
    with open(runs_path, "r", encoding="utf-8") as f:
        for line in f:
            if line.strip():
                runs.append(json.loads(line))
                
    if args.limit:
        runs = runs[:args.limit]
        
    import time
    from concurrent.futures import ThreadPoolExecutor, as_completed

    evaluator_rpm = 250
    evaluator_max_workers = 15
    pacing_delay = 60.0 / evaluator_rpm

    results_map = {}
    
    with ThreadPoolExecutor(max_workers=evaluator_max_workers) as executor:
        future_to_idx = {}
        for idx, run in enumerate(runs):
            print(f"Submitting retrieval evaluation {idx+1}/{len(runs)} (run_id: {run.get('run_id')})...")
            future = executor.submit(evaluate_run, run, config, args.model)
            future_to_idx[future] = idx
            time.sleep(pacing_delay)
            
        for future in as_completed(future_to_idx):
            idx = future_to_idx[future]
            try:
                res = future.result()
                results_map[idx] = res
                print(f"Completed retrieval evaluation {idx+1}/{len(runs)} (run_id: {res.get('run_id')}).")
            except Exception as e:
                print(f"Error in retrieval evaluation {idx+1}: {e}")
                
    results = [results_map[k] for k in sorted(results_map.keys())]
        
    with open(out_path, "w", encoding="utf-8") as f:
        for r in results:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
            
    print(f"Evaluated {len(results)} runs. Retrieval scores saved to {out_path}")

if __name__ == "__main__":
    main()
